package model;

import java.util.ArrayList;

/**
 *
 * @author satrio
 */
public class AnggotaPerpustakaan {
    private String id;
    private String nama;
    private String alamat;
    private ArrayList<Transaksi> listTransaksi = new ArrayList<>();

    public AnggotaPerpustakaan(String id, String nama, String alamat) {
        this.id = id;
        this.nama = nama;
        this.alamat = alamat;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public ArrayList<Transaksi> getListTransaksi() {
        return listTransaksi;
    }

    public void setListTransaksi(ArrayList<Transaksi> listTransaksi) {
        this.listTransaksi = listTransaksi;
    }
    
}   

